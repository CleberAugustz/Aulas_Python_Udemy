#OPERADORES RELACIONAIS

n1 = int(input('Digite um número:\n'))
n2 = int(input('Digite um segundo número:\n'))

if (n1 == n2):#Operador de igualdade
    print('O número {} é igual ao {}'.format(n1,n2))

if(n1 != n2):#Operador de diferença
    print('O número {] é diferente do número {}'.format(n1,n2))

if(n1 < n2):#Operador de Menor que
    print('O número {} é menor que o número {}'.format(n1,n2))
if(n1 > n2):#Operador Maior que
    print('O número {} é maior que o número {}'.format(n1,n2))
if(n1 >= n2 ):#Operador Maior ou igual
    print('O número {} é maior ou igual a {}'.format(n1,n2))
if(n1 <= n2 ):#Operador Menor ou Igual
    print('O número {} é menor ou igual a {}'.format(n1,n2))